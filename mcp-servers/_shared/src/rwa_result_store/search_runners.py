# OpenAlex
# ===================================================================

_OPENALEX_BASE = "https://api.openalex.org"


def _format_openalex_work(work: dict[str, Any]) -> dict[str, Any]:
    authorships = work.get("authorships", [])
    authors = []
    for a in authorships:
        name = a.get("author", {}).get("display_name", "")
        if name:
            authors.append(name)
    primary_location = work.get("primary_location") or {}
    source = primary_location.get("source") or {}
    return {
        "openalex_id": work.get("id", ""),
        "doi": work.get("doi", ""),
        "title": work.get("title", ""),
        "publication_year": work.get("publication_year"),
        "type": work.get("type", ""),
        "authors": authors,
        "journal": source.get("display_name", ""),
        "is_oa": work.get("open_access", {}).get("is_oa", False),
        "oa_url": work.get("open_access", {}).get("oa_url", ""),
        "cited_by_count": work.get("cited_by_count", 0),
        "abstract_inverted_index": bool(work.get("abstract_inverted_index")),
        "concepts": [
            {"name": c.get("display_name", ""), "score": c.get("score", 0)}
            for c in (work.get("concepts") or [])[:5]
        ],
        "pmid": work.get("ids", {}).get("pmid", ""),
    }


def run_openalex(
    project_path: str,
    query: str,
    filters: str | None = None,
    sort: str = "relevance_score:desc",
    per_page: int = 20,
) -> int:
    """Run an OpenAlex search and store results. Returns search_id or 0."""
    base_params: dict[str, str] = {}
    api_key = os.environ.get("OPENALEX_API_KEY", "")
    if api_key:
        base_params["api_key"] = api_key
    params = {
        **base_params,
        "search": query,
        "sort": sort,
        "per_page": str(per_page),
    }
    if filters:
        params["filter"] = filters

    with httpx.Client(timeout=30.0) as client:
        resp = client.get(f"{_OPENALEX_BASE}/works", params=params)
        resp.raise_for_status()
        data = resp.json()

    works = [_format_openalex_work(w) for w in data.get("results", [])]
    total_count = data.get("meta", {}).get("count", 0)

    if not works:
        return 0

    search_id = store_results(
        project_path,
        "openalex",
        query,
        works,
        total_count=total_count,
        parameters={"filters": filters, "sort": sort, "per_page": per_page},
    )
    export_results_excel(project_path)
    return search_id


