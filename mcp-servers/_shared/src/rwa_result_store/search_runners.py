# Europe PMC
# ===================================================================

_EPMC_BASE = "https://www.ebi.ac.uk/europepmc/webservices/rest"


def _format_epmc_result(r: dict[str, Any]) -> dict[str, Any]:
    return {
        "id": r.get("id", ""),
        "source": r.get("source", ""),
        "pmid": r.get("pmid", ""),
        "pmcid": r.get("pmcid", ""),
        "doi": r.get("doi", ""),
        "title": r.get("title", ""),
        "authors": r.get("authorString", ""),
        "journal": r.get("journalTitle", ""),
        "year": r.get("pubYear", ""),
        "abstract": r.get("abstractText", ""),
        "is_open_access": r.get("isOpenAccess", "N") == "Y",
        "cited_by_count": r.get("citedByCount", 0),
        "first_publication_date": r.get("firstPublicationDate", ""),
        "publication_type": r.get("pubType", ""),
    }


def run_europe_pmc(
    project_path: str,
    query: str,
    result_type: str = "core",
    page_size: int = 25,
    sort: str = "relevance",
    open_access_only: bool = False,
) -> int:
    """Run a Europe PMC search and store results. Returns search_id or 0."""
    full_query = query
    if open_access_only:
        full_query += " AND OPEN_ACCESS:y"

    sort_map = {
        "relevance": "RELEVANCE",
        "date": "P_PDATE_D desc",
        "cited": "CITED desc",
    }
    params: dict[str, str] = {
        "query": full_query,
        "resultType": result_type,
        "pageSize": str(page_size),
        "sort": sort_map.get(sort, "RELEVANCE"),
        "format": "json",
    }

    with httpx.Client(timeout=30.0) as client:
        resp = client.get(f"{_EPMC_BASE}/search", params=params)
        resp.raise_for_status()
        data = resp.json()

    result_list = data.get("resultList", {}).get("result", [])
    results = [_format_epmc_result(r) for r in result_list]
    total_count = data.get("hitCount", 0)

    if not results:
        return 0

    search_id = store_results(
        project_path,
        "europe_pmc",
        query,
        results,
        total_count=total_count,
        parameters={
            "result_type": result_type,
            "page_size": page_size,
            "sort": sort,
            "open_access_only": open_access_only,
        },
    )
    export_results_excel(project_path)
    return search_id


# ===================================================================
# CrossRef
# ===================================================================

_CROSSREF_BASE = "https://api.crossref.org"


def _format_crossref_work(item: dict[str, Any]) -> dict[str, Any]:
    authors = []
    for a in item.get("author", []):
        given = a.get("given", "")
        family = a.get("family", "")
        name = f"{family}, {given}" if given else family
        if name:
            authors.append(name)

    titles = item.get("title", [])
    title = titles[0] if titles else ""
    containers = item.get("container-title", [])
    journal = containers[0] if containers else ""
    date_parts = item.get("published-print", {}).get("date-parts", [[]])
    if not date_parts or not date_parts[0]:
        date_parts = item.get("published-online", {}).get("date-parts", [[]])
    year = str(date_parts[0][0]) if date_parts and date_parts[0] else ""

    return {
        "doi": item.get("DOI", ""),
        "title": title,
        "authors": authors,
        "journal": journal,
        "year": year,
        "volume": item.get("volume", ""),
        "issue": item.get("issue", ""),
        "page": item.get("page", ""),
        "type": item.get("type", ""),
        "publisher": item.get("publisher", ""),
        "issn": item.get("ISSN", []),
        "is_referenced_by_count": item.get("is-referenced-by-count", 0),
        "references_count": item.get("references-count", 0),
        "url": item.get("URL", ""),
        "abstract": item.get("abstract", ""),
    }


def run_crossref(
    project_path: str,
    query: str,
    filters: str | None = None,
    rows: int = 20,
    sort: str = "relevance",
) -> int:
    """Run a CrossRef search and store results. Returns search_id or 0."""
    base_params: dict[str, str] = {}
    email = os.environ.get("CROSSREF_EMAIL", "")
    if email:
        base_params["mailto"] = email
    params = {
        **base_params,
        "query": query,
        "rows": str(rows),
        "sort": sort,
    }
    if filters:
        params["filter"] = filters

    with httpx.Client(timeout=30.0) as client:
        resp = client.get(f"{_CROSSREF_BASE}/works", params=params)
        resp.raise_for_status()
        data = resp.json()

    message = data.get("message", {})
    items = message.get("items", [])
    works = [_format_crossref_work(item) for item in items]
    total_count = message.get("total-results", 0)

    if not works:
        return 0

    search_id = store_results(
        project_path,
        "crossref",
        query,
        works,
        total_count=total_count,
        parameters={"filters": filters, "rows": rows, "sort": sort},
    )
    export_results_excel(project_path)
    return search_id
