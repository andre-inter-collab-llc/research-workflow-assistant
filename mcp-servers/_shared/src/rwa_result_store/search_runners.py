"""Synchronous search runner functions for reproducible database searches.

Each runner function:
- Makes synchronous HTTP requests via ``httpx.Client``
- Stores results in the project's SQLite database via ``rwa_result_store``
- Returns the ``search_id`` (positive int) on success, or ``0`` if no results
- Reads API keys from environment variables (never hardcoded)

Designed to be called from thin stub scripts generated by
``script_templates.py``, or directly from Python code.
"""

from __future__ import annotations

import os
import time
import xml.etree.ElementTree as ET
from typing import Any

import httpx

from . import export_results_excel, store_results

# ===================================================================
# PubMed
# ===================================================================

_EUTILS_BASE = "https://eutils.ncbi.nlm.nih.gov/entrez/eutils"


def _pubmed_base_params() -> dict[str, str]:
    params: dict[str, str] = {"retmode": "xml"}
    api_key = os.environ.get("NCBI_API_KEY", "")
    if api_key:
        params["api_key"] = api_key
    return params


def _parse_esearch_ids(xml_text: str) -> tuple[list[str], int]:
    root = ET.fromstring(xml_text)
    count_el = root.find("Count")
    count = int(count_el.text) if count_el is not None and count_el.text else 0
    ids = [id_el.text for id_el in root.findall(".//IdList/Id") if id_el.text]
    return ids, count


def _parse_esummary(xml_text: str) -> list[dict[str, Any]]:
    root = ET.fromstring(xml_text)
    articles: list[dict[str, Any]] = []
    for doc in root.findall(".//DocSum"):
        article: dict[str, Any] = {}
        id_el = doc.find("Id")
        if id_el is not None and id_el.text:
            article["pmid"] = id_el.text
        for item in doc.findall("Item"):
            name = item.get("Name", "")
            if name in (
                "Title",
                "Source",
                "PubDate",
                "FullJournalName",
                "DOI",
                "Volume",
                "Issue",
                "Pages",
            ):
                article[name.lower()] = item.text or ""
            elif name == "AuthorList":
                authors = [a.text for a in item.findall("Item") if a.text]
                article["authors"] = authors
        articles.append(article)
    return articles


def run_pubmed(
    project_path: str,
    query: str,
    max_results: int = 20,
    date_range: str | None = None,
    article_types: str | None = None,
) -> int:
    """Run a PubMed search and store results. Returns search_id or 0."""
    full_query = query
    if article_types:
        full_query += f" AND {article_types}[pt]"

    with httpx.Client(timeout=30.0) as client:
        search_params = {
            **_pubmed_base_params(),
            "db": "pubmed",
            "term": full_query,
            "retmax": str(max_results),
        }
        if date_range and ":" in date_range:
            parts = date_range.split(":")
            search_params["mindate"] = parts[0]
            search_params["maxdate"] = parts[1]
            search_params["datetype"] = "pdat"

        resp = client.get(f"{_EUTILS_BASE}/esearch.fcgi", params=search_params)
        resp.raise_for_status()
        pmids, total_count = _parse_esearch_ids(resp.text)

        if not pmids:
            return 0

        summary_params = {
            **_pubmed_base_params(),
            "db": "pubmed",
            "id": ",".join(pmids),
        }
        resp = client.get(f"{_EUTILS_BASE}/esummary.fcgi", params=summary_params)
        resp.raise_for_status()
        articles = _parse_esummary(resp.text)

    search_id = store_results(
        project_path,
        "pubmed",
        query,
        articles,
        total_count=total_count,
        parameters={
            "max_results": max_results,
            "date_range": date_range,
            "article_types": article_types,
        },
    )
    export_results_excel(project_path)
    return search_id


