_S2_BASE = "https://api.semanticscholar.org/graph/v1"

_S2_PAPER_FIELDS = ",".join(
    [
        "paperId",
        "externalIds",
        "title",
        "abstract",
        "year",
        "venue",
        "publicationVenue",
        "authors",
        "citationCount",
        "referenceCount",
        "isOpenAccess",
        "openAccessPdf",
        "fieldsOfStudy",
        "tldr",
    ]
)

_S2_MAX_RETRIES = 3
_S2_BASE_BACKOFF = 1.0


def _s2_headers() -> dict[str, str]:
    headers: dict[str, str] = {}
    api_key = os.environ.get("S2_API_KEY", "")
    if api_key:
        headers["x-api-key"] = api_key
    return headers


def _request_with_backoff(
    client: httpx.Client,
    method: str,
    url: str,
    **kwargs: Any,
) -> httpx.Response:
    """Make an HTTP request with exponential backoff on 429 (rate limit)."""
    for attempt in range(_S2_MAX_RETRIES + 1):
        resp = client.request(method, url, headers=_s2_headers(), **kwargs)
        if resp.status_code != 429 or attempt == _S2_MAX_RETRIES:
            resp.raise_for_status()
            return resp
        wait = _S2_BASE_BACKOFF * (2**attempt)
        time.sleep(wait)
    return resp  # unreachable; satisfies type checker


def _format_s2_paper(paper: dict[str, Any]) -> dict[str, Any]:
    external_ids = paper.get("externalIds") or {}
    tldr = paper.get("tldr") or {}
    oa_pdf = paper.get("openAccessPdf") or {}
    pub_venue = paper.get("publicationVenue") or {}
    return {
        "paper_id": paper.get("paperId", ""),
        "doi": external_ids.get("DOI", ""),
        "pmid": external_ids.get("PubMed", ""),
        "arxiv_id": external_ids.get("ArXiv", ""),
        "title": paper.get("title", ""),
        "abstract": paper.get("abstract", ""),
        "year": paper.get("year"),
        "venue": pub_venue.get("name", "") or paper.get("venue", ""),
        "authors": [
            {"name": a.get("name", ""), "author_id": a.get("authorId", "")}
            for a in (paper.get("authors") or [])
        ],
        "citation_count": paper.get("citationCount", 0),
        "reference_count": paper.get("referenceCount", 0),
        "is_open_access": paper.get("isOpenAccess", False),
        "open_access_pdf": oa_pdf.get("url", ""),
        "fields_of_study": paper.get("fieldsOfStudy") or [],
        "tldr": tldr.get("text", ""),
    }


def run_semantic_scholar(
    project_path: str,
    query: str,
    year_range: str | None = None,
    fields_of_study: str | None = None,
    open_access_only: bool = False,
    limit: int = 20,
) -> int:
    """Run a Semantic Scholar search and store results. Returns search_id or 0."""
    params: dict[str, str] = {
        "query": query,
        "limit": str(limit),
        "fields": _S2_PAPER_FIELDS,
    }
    if year_range and "-" in year_range:
        params["year"] = year_range
    if fields_of_study:
        params["fieldsOfStudy"] = fields_of_study
    if open_access_only:
        params["openAccessPdf"] = ""

    with httpx.Client(timeout=30.0) as client:
        resp = _request_with_backoff(
            client,
            "GET",
            f"{_S2_BASE}/paper/search",
            params=params,
        )
        data = resp.json()

    papers = [_format_s2_paper(p) for p in data.get("data", [])]
    total = data.get("total", 0)

    if not papers:
        return 0

    search_id = store_results(
        project_path,
        "semantic_scholar",
        query,
        papers,
        total_count=total,
        parameters={
            "year_range": year_range,
            "fields_of_study": fields_of_study,
            "open_access_only": open_access_only,
            "limit": limit,
        },
    )
    export_results_excel(project_path)
    return search_id


