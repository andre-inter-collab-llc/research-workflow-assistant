"""Thin script generators for reproducible database searches.

Each function returns a short Python script (~20 lines) that imports
a runner function from ``rwa_result_store.search_runners`` and invokes it.

The generated script resolves the project path relative to its own location
(``Path(__file__).resolve().parent.parent``), making it portable across machines.

Requires ``rwa-shared`` to be installed (``pip install -e mcp-servers/_shared``).
"""

from __future__ import annotations

import json
from datetime import UTC, datetime


def _format_kwargs(kwargs: dict) -> str:
    """Format keyword arguments as lines of Python source for the stub script."""
    lines = []
    for k, v in kwargs.items():
        lines.append(f"    {k}={v!r},")
    return "\n".join(lines)


def _render_stub(
    source: str,
    runner_name: str,
    query: str,
    parameters: dict,
    call_kwargs: str,
) -> str:
    """Build a complete thin stub script string."""
    ts = datetime.now(UTC).isoformat()
    param_summary = json.dumps(parameters, default=str)
    safe_query = query.replace('"""', "'''")
    return (
        '#!/usr/bin/env python3\n'
        '# Requires: pip install rwa-shared  (which installs httpx)\n'
        '"""Reproducible search script generated by the Research Workflow Assistant.\n'
        '\n'
        f'Source  : {source}\n'
        f'Query   : {safe_query}\n'
        f'Created : {ts}\n'
        f'Params  : {param_summary}\n'
        '"""\n'
        'from pathlib import Path\n'
        '\n'
        f'from rwa_result_store.search_runners import {runner_name}\n'
        '\n'
        'PROJECT_PATH = str(Path(__file__).resolve().parent.parent)\n'
        f'search_id = {runner_name}(\n'
        '    project_path=PROJECT_PATH,\n'
        f'{call_kwargs}\n'
        ')\n'
        'print(search_id if search_id else "NO_RESULTS")\n'
    )


# ===================================================================
# PubMed
# ===================================================================

def pubmed_script(
    query: str,
    max_results: int = 20,
    date_range: str | None = None,
    article_types: str | None = None,
    project_path: str = ".",
) -> str:
    """Return a thin PubMed search stub script."""
    params = {
        "max_results": max_results,
        "date_range": date_range,
        "article_types": article_types,
    }
    kwargs = {"query": query, **params}
    return _render_stub("pubmed", "run_pubmed", query, params, _format_kwargs(kwargs))


